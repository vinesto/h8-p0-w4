function countProfit(shoppers) {
  let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                     ['Baju Zoro', 500000, 2],
                     ['Sweater Uniklooh', 175000, 1]
                   ];

  var result=[];
  for(var i=0; i<listBarang.length; i++){
    var barang={
      product:listBarang[i][0],
      shoppers: [],
      leftOver:listBarang[i][2],
      totalProfit:listBarang[i][1],
    }
    result.push(barang);
    for(var j=0; j<shoppers.length; j++){
        // console.log(shoppers[j].product)
      if(result[i].product===shoppers[j].product){
        if(result[i].leftOver >= shoppers[j].amount){
          result[i].shoppers.push(shoppers[j].name);
          result[i].leftOver -= shoppers[j].amount;
        }
      }
    }
  }
  for(var k=0; k<result.length; k++){
    if(result[k].shoppers !== []){
      result[k].totalProfit*=(listBarang[k][2]-result[k].leftOver);
    }else{
      result[k].totalProfit=0;
    }
  }

  if(shoppers.length === 0){
    result=[];
  }
  return result
}

